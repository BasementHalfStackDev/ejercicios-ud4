
public class ejercicio_2 {

	public static void main(String[] args) {
		
		// Iniciando variables de diferentes tipos
		
		int N = 12;
		double A = 4.3;
		char C = 'Z';
		
		// Muestro el valor de las variables, la suma y la resta
		
		System.out.println("El valor de N es: "+N+", el valor de A es: "+A+" y el valor de C es: "+C+".");
		System.out.println("La suma de N + A es: "+(N+A));
		System.out.println("La diferencia de A - N es de: "+(A-N));
		
		// Consigo el valor numerico del caracter almacenado en la variable C
		
		int zValue = C;
		
		// Muestro dicho valor
		
		System.out.println("El valor numerico del caracter "+C+" es: "+zValue);
		

	}

}
