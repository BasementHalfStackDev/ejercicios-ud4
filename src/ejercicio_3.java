
public class ejercicio_3 {

	public static void main(String[] args) {
		
		int X = 30;
		int Y = 7;
		
		double N = 9.32;
		double M = 3.14;
		
		System.out.println("Los valores de las variables X Y N M son: "+X+", "+Y+", "+N+" y "+M);
		System.out.println();
		
		//Operaciones con variables X e Y
		
		System.out.println("La suma de "+X+" mas "+Y+" es: "+(X+Y));
		System.out.println("La diferencia de "+X+" menos "+Y+" es: "+(X-Y));
		System.out.println("El producto de "+X+" multiplicado por "+Y+" es: "+(X*Y));
		System.out.println("El cociente de "+X+" dividido por "+Y+" es: "+(X/Y));
		System.out.println("El resto de "+X+" dividido por "+Y+" es: "+(X%Y));
		System.out.println();
		
		// Operaciones con variables N y M
		
		System.out.println("La suma de "+N+" mas "+M+" es: "+(N+M));
		System.out.println("La diferencia de "+N+" menos "+M+" es: "+(N-M));
		System.out.println("El producto de "+N+" multiplicado por "+M+" es: "+(N*M));
		System.out.println("El cociente de "+N+" dividido por "+M+" es: "+(N/M));
		System.out.println("El resto de "+N+" dividido por "+M+" es: "+(N%M));
		System.out.println();
		
		// Popurr√≠
		
		System.out.println("La suma de "+X+" mas "+N+" es: "+(X+N));
		System.out.println("El cociente de "+Y+" dividido por "+M+" es: "+(Y/M));
		System.out.println("El resto de "+Y+" dividido por "+M+" es: "+(Y%M));
		System.out.println();
		
		// Operaciones con todas las variables
		
		System.out.println("El doble de las variables "+X+", "+Y+", "+N+" y "+M+" es de: "+(X*2)+", "+(Y*2)+", "+(N*2)+" y "+(M*2)+" respectivamente.");
		System.out.println("La suma de las variables "+X+", "+Y+", "+N+" y "+M+" es de: "+(X+Y+N+M));
		System.out.println("El producto de las variables "+X+", "+Y+", "+N+" y "+M+" es de: "+(X*Y*N*M));
		
		System.out.println();
		System.out.println("FIN DEL PROGRAMA.");
		
	}

}
